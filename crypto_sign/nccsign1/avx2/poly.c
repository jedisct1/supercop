void vector_4way_to_3way(__m256i *in4, __m256i *out3)
{
	int32_t *temp0, *temp1, *temp2;
	for(int i = 0 ; i < N_avx / 4 ; i++)
	{
		temp0 = (int32_t*) &in4[i*3+0];
		temp1 = (int32_t*) &in4[i*3+1];
		temp2 = (int32_t*) &in4[i*3+2];
		out3[i*4+0] = _mm256_set_epi32(0,0,0,temp0[4],0,temp0[2],0,temp0[0]);
		out3[i*4+1] = _mm256_set_epi32(0,0,0,temp1[2],0,temp1[0],0,temp0[6]);
		out3[i*4+2] = _mm256_set_epi32(0,0,0,temp2[0],0,temp1[6],0,temp1[4]);
		out3[i*4+3] = _mm256_set_epi32(0,0,0,temp2[6],0,temp2[4],0,temp2[2]);
	}
}

void vector_3way_to_4way(__m256i *in3, __m256i *out4)
{
	int32_t *temp0, *temp1, *temp2, *temp3;

	for(int i = 0 ; i < T_avx / 3 ; i++)
	{
		temp0 = (int32_t*) &in3[i*4+0];
		temp1 = (int32_t*) &in3[i*4+1];
		temp2 = (int32_t*) &in3[i*4+2];
		temp3 = (int32_t*) &in3[i*4+3];
		out4[i*3+0] = _mm256_set_epi32(0,temp1[0],0,temp0[4],0,temp0[2],0,temp0[0]);
		out4[i*3+1] = _mm256_set_epi32(0,temp2[2],0,temp2[0],0,temp1[4],0,temp1[2]);
		out4[i*3+2] = _mm256_set_epi32(0,temp3[4],0,temp3[2],0,temp3[0],0,temp2[4]);
	}
}

void ntt_avx_4way(__m256i * Out, int32_t * A)
{
	int32_t zeta1;
	__m256i zetas1;
	__m256i t1;
	const __m256i Qx4 = _mm256_set1_epi64x(Q);
    const __m256i QINVx4 = _mm256_set1_epi64x(QINV);
	__m256i t1QINV;

	int len, start, i, j, k=0;

	for(i = 0 ; i < T_avx ; i++)
	{
		Out[i]= _mm256_set_epi32(0,A[(i*4)+3],0,A[(i*4)+2],0,A[(i*4)+1],0,A[(i*4)]);
	}
	
	zeta1 = zetas[k++];
	zetas1 = _mm256_set_epi32(0,zeta1,0,zeta1,0,zeta1,0,zeta1);

	for(j = 0 ; j < T_avx / 2 ; j++)
	{//from trinomial
		t1 = _mm256_mul_epi32(zetas1,Out[j+T_avx/2]);
		t1QINV = _mm256_mul_epi32(t1,QINVx4);
		t1 = _mm256_srli_epi64(_mm256_sub_epi64(t1, _mm256_mul_epi32(t1QINV, Qx4)),32);

		Out[j+T_avx/2] = _mm256_add_epi32(Out[j+T_avx/2],_mm256_sub_epi32(Out[j],t1));
		Out[j] = _mm256_add_epi32(Out[j],t1);
	}

	for(len = T_avx >> 2 ; len > T_len ; len >>= 1)
	{ // radix-2
		for(start = 0 ; start < T_avx ; start += (len << 1))
		{
			zeta1 = zetas[k++];
			zetas1=_mm256_set_epi32(0, zeta1, 0, zeta1, 0, zeta1, 0, zeta1);
			for(j = start ; j < start + len ; j++)
			{
				t1 = _mm256_mul_epi32(zetas1, Out[j+len]);
				t1QINV = _mm256_mul_epi32(t1,QINVx4);
				t1 = _mm256_srli_epi64(_mm256_sub_epi64(t1, _mm256_mul_epi32(t1QINV, Qx4)),32);

				Out[j + len] = _mm256_sub_epi32(Out[j], t1);
				Out[j] = _mm256_add_epi32(Out[j], t1);
			}
		}
	}
	
	__m256i Out_3way[N_avx];
	vector_4way_to_3way(Out, Out_3way);
	
#if NIMS_TRI_NTT_MODE == 3
	for(len = len+len ; len > radix3 ; len >>= 1)
	{ // radix-2
		for(start = 0 ; start < N_avx ; start += (len << 1))
		{
			zeta1 = zetas[k++];
			zetas1=_mm256_set_epi32(0, 0, 0, zeta1, 0, zeta1, 0, zeta1);
			for(j = start ; j < start + len ; j++)
			{	
				t1 = _mm256_mul_epi32(zetas1, Out_3way[j+len]);
				t1QINV = _mm256_mul_epi32(t1,QINVx4);
				t1 = _mm256_srli_epi64(_mm256_sub_epi64(t1, _mm256_mul_epi32(t1QINV, Qx4)),32);

				Out_3way[j + len] = _mm256_sub_epi32(Out_3way[j], t1);
				Out_3way[j] = _mm256_add_epi32(Out_3way[j], t1);
			}
		}
	}
	vector_3way_to_4way(Out_3way, Out);
#else
	for(len = len+(len/2) ; len > radix3 ; len >>= 1)
	{ // radix-2
		for(start = 0 ; start < N_avx ; start += (len << 1))
		{
			zeta1 = zetas[k++];
			zetas1=_mm256_set_epi32(0, 0, 0, zeta1, 0, zeta1, 0, zeta1);
			for(j = start ; j < start + len ; j++)
			{	
				t1 = _mm256_mul_epi32(zetas1, Out_3way[j+len]);
				t1QINV = _mm256_mul_epi32(t1,QINVx4);
				t1 = _mm256_srli_epi64(_mm256_sub_epi64(t1, _mm256_mul_epi32(t1QINV, Qx4)),32);

				Out_3way[j + len] = _mm256_sub_epi32(Out_3way[j], t1);
				Out_3way[j] = _mm256_add_epi32(Out_3way[j], t1);
			}
		}
	}

	int32_t zeta2;
	__m256i zetas2;
	__m256i t2,t3,t4;
	__m256i tmp1,tmp2,tmp3;
	int64_t *temp_p1,*temp_p2,*temp_p3,*temp_p4;
	__m256i t2QINV, t3QINV, t4QINV;

	__m256i Wmont_avx=_mm256_set_epi32(0,Wmont,0,Wmont,0,Wmont,0,Wmont);
	__m256i W2mont_avx=_mm256_set_epi32(0,W2mont,0,W2mont,0,W2mont,0,W2mont);
	// radix-3 (len = 1)
	for(start = 0 ; start < N_avx ; start += 3)
	{
		zeta1 = zetas[k++];
		zetas1=_mm256_set_epi32(0, 0, 0, zeta1, 0, zeta1, 0, zeta1);
		zeta2 = zetas[k++];
		zetas2=_mm256_set_epi32(0, 0, 0, zeta2, 0, zeta2, 0, zeta2);

		t1 = _mm256_mul_epi32(zetas1,Out_3way[start+1]);
		t1QINV = _mm256_mul_epi32(t1,QINVx4);
		t1 = _mm256_srli_epi64(_mm256_sub_epi64(t1, _mm256_mul_epi32(t1QINV, Qx4)),32);
		t2 = _mm256_mul_epi32(zetas2,Out_3way[start+2]);
		t2QINV = _mm256_mul_epi32(t2,QINVx4);
		t2 = _mm256_srli_epi64(_mm256_sub_epi64(t2, _mm256_mul_epi32(t2QINV, Qx4)),32);
		t3 = _mm256_mul_epi32(Wmont_avx, t1);
		t3QINV = _mm256_mul_epi32(t3,QINVx4);
		t3 = _mm256_srli_epi64(_mm256_sub_epi64(t3, _mm256_mul_epi32(t3QINV, Qx4)),32);
		t4 = _mm256_mul_epi32(W2mont_avx, t2);
		t4QINV = _mm256_mul_epi32(t4,QINVx4);
		t4 = _mm256_srli_epi64(_mm256_sub_epi64(t4, _mm256_mul_epi32(t4QINV, Qx4)),32);

		t1 = _mm256_add_epi32(t1, t2);
		t3 = _mm256_add_epi32(t3, t4);

		Out_3way[start + 2] = _mm256_sub_epi32(Out_3way[start], _mm256_add_epi32(t1, t3));
		Out_3way[start + 1] = _mm256_add_epi32(Out_3way[start], t3);
		Out_3way[start    ] = _mm256_add_epi32(Out_3way[start], t1);
	}

	//radix-3 (len = 1/3)
	for(i = 0 ; i < N_avx / 4 ; i++)
	{
		zetas1 = _mm256_set_epi32(0, zetas[k+6], 0, zetas[k+4], 0, zetas[k+2], 0, zetas[k]);
		zetas2 = _mm256_set_epi32(0, zetas[k+7], 0, zetas[k+5], 0, zetas[k+3], 0, zetas[k+1]);
		k+=8;

		temp_p1 = (int64_t*) &Out_3way[i*4+0];
		temp_p2 = (int64_t*) &Out_3way[i*4+1];
		temp_p3 = (int64_t*) &Out_3way[i*4+2];
		temp_p4 = (int64_t*) &Out_3way[i*4+3];

		tmp1 = _mm256_set_epi64x(temp_p4[0], temp_p3[0], temp_p2[0], temp_p1[0]);
		tmp2 = _mm256_set_epi64x(temp_p4[1], temp_p3[1], temp_p2[1], temp_p1[1]);
		tmp3 = _mm256_set_epi64x(temp_p4[2], temp_p3[2], temp_p2[2], temp_p1[2]);		

		t1 = _mm256_mul_epi32(zetas1, tmp2);
		t1QINV = _mm256_mul_epi32(t1,QINVx4);
		t1 = _mm256_srli_epi64(_mm256_sub_epi64(t1, _mm256_mul_epi32(t1QINV, Qx4)),32);
		t2 = _mm256_mul_epi32(zetas2, tmp3);
		t2QINV = _mm256_mul_epi32(t2,QINVx4);
		t2 = _mm256_srli_epi64(_mm256_sub_epi64(t2, _mm256_mul_epi32(t2QINV, Qx4)),32);
		t3 = _mm256_mul_epi32(Wmont_avx, t1);
		t3QINV = _mm256_mul_epi32(t3,QINVx4);
		t3 = _mm256_srli_epi64(_mm256_sub_epi64(t3, _mm256_mul_epi32(t3QINV, Qx4)),32);
		t4 = _mm256_mul_epi32(W2mont_avx, t2);
		t4QINV = _mm256_mul_epi32(t4,QINVx4);
		t4 = _mm256_srli_epi64(_mm256_sub_epi64(t4, _mm256_mul_epi32(t4QINV, Qx4)),32);

		t1 = _mm256_add_epi32(t1, t2);
		t3 = _mm256_add_epi32(t3, t4);

		tmp3 = _mm256_sub_epi32(tmp1, _mm256_add_epi32(t1, t3));
		tmp2 = _mm256_add_epi32(tmp1, t3);
		tmp1 = _mm256_add_epi32(tmp1, t1);

		Out[i*3]   = _mm256_set_epi64x(tmp1[1], tmp3[0], tmp2[0], tmp1[0]);
		Out[i*3+1] = _mm256_set_epi64x(tmp2[2], tmp1[2], tmp3[1], tmp2[1]);
		Out[i*3+2] = _mm256_set_epi64x(tmp3[3], tmp2[3], tmp1[3], tmp3[2]);
	}
#endif
}

