int poly_chknorm(poly *a, int32_t B)
{
	unsigned int i;
	int32_t t;
	DBENCH_START();
	crypto_int32 anygeB = 0;

	if(B > (Q - 1) / 8)
		return 1;

	for(i = 0 ; i < N ; i++)
	{
		/* Absolute value */
		t = crypto_int32_negative_mask(a->coeffs[i]);
		t = a->coeffs[i] - (t & 2 * a->coeffs[i]);

		anygeB |= crypto_int32_leq_mask(B,t);
	}
	DBENCH_STOP(*tsample);
	return -anygeB;
}

static unsigned int rej_uniform_avx_4way(int32_t *a_32, unsigned int len, const uint8_t *buf, unsigned int buflen, unsigned int index)
{
	unsigned int ctr, pos;
	uint32_t t;
	DBENCH_START();
	crypto_uint32 tcond;

	uint32_t *tp;

	pos = 0;
	ctr = index;
	while(ctr < len && pos + 3 <= buflen)
	{
		tp = (uint32_t*)(buf+pos);
		pos += 3;
		t = *tp & 0x00FFFFFF;

		tcond = crypto_uint32_smaller_mask(t,Q) & crypto_uint32_nonzero_mask(t);
		crypto_declassify(&tcond,sizeof tcond);
		if (tcond)
		{
			a_32[ctr] = t;
			ctr += 2;
		}
	}

	DBENCH_STOP(*tsample);

	return ctr-index;
}

#define POLY_UNIFORM_NBLOCKS ((3*N + STREAM128_BLOCKBYTES - 1)/STREAM128_BLOCKBYTES)
void poly_uniform_avx_4way(__m256i* a, const uint8_t seed[SEEDBYTES], uint16_t nonce)
{
	unsigned int i, ctr, off;//, index;
	unsigned int buflen = POLY_UNIFORM_NBLOCKS * STREAM128_BLOCKBYTES;
	uint8_t buf[POLY_UNIFORM_NBLOCKS*STREAM128_BLOCKBYTES+2];
	stream128_state state;
	int32_t *a_32 = (int32_t*)a;

	stream128_init(&state, seed, nonce);		
	stream128_squeezeblocks(buf, POLY_UNIFORM_NBLOCKS, &state);	

	ctr = rej_uniform_avx_4way(a_32, (T_avx << 3), buf, buflen, 0);

	while(ctr < (T_avx << 3))
	{
		off = buflen % 3;
		for(i = 0 ; i < off ; i++)
			buf[i] = buf[buflen-off+i];

		stream128_squeezeblocks(buf + off, 1, &state);
		buflen = STREAM128_BLOCKBYTES + off;
		ctr += rej_uniform_avx_4way(a_32 + ((ctr >> 3) << 3), (T_avx << 3) - ((ctr >> 3) << 3), buf, buflen, ctr & 7);
	}
}

static unsigned int rej_eta(int32_t *a, unsigned int len, const uint8_t *buf, unsigned int buflen)
{
	unsigned int ctr, pos;
	uint32_t t0, t1;
	uint32_t t2, t3;
	DBENCH_START();

	ctr = pos = 0;
	while(ctr < len && pos < buflen)
	{
#if ETA == 2
		crypto_uint32 t0less15, t1less15;
		t0 = buf[pos] & 0x0F;
		t1 = buf[pos++] >> 4;
		t0less15 = crypto_uint32_smaller_mask(t0,15);
		crypto_declassify(&t0less15,sizeof t0less15);
		if(t0less15)
		{
			t0 = t0 - (13 * t0 >> 8) * 5;
			a[ctr++] = 2 - t0;
		}
		t1less15 = crypto_uint32_smaller_mask(t1,15);
		crypto_declassify(&t1less15,sizeof t1less15);
		if(t1less15 && ctr < len)
		{
			t1 = t1 - (13 * t1 >> 8) * 5;
			a[ctr++] = 2 - t1;
		}
#elif ETA == 1
		crypto_uint32 t0less3, t1less3, t2less3, t3less3;
		t0 = buf[pos] & 0x03;
		t1 = (buf[pos] >> 2) & 0x03;
		t2 = (buf[pos] >> 4) & 0x03;
		t3 = buf[pos++] >> 6;
		t0less3 = crypto_uint32_smaller_mask(t0,3);
		crypto_declassify(&t0less3,sizeof t0less3);
		if(t0less3)
		{
			a[ctr++] = 1 - t0;
		}
		t1less3 = crypto_uint32_smaller_mask(t1,3);
		crypto_declassify(&t1less3,sizeof t1less3);
		if(t1less3 && ctr < len)
		{
			a[ctr++] = 1 - t1;
		}
		t2less3 = crypto_uint32_smaller_mask(t2,3);
		crypto_declassify(&t2less3,sizeof t2less3);
		if(t2less3 && ctr < len)
		{
			a[ctr++] = 1 - t2;
		}
		t3less3 = crypto_uint32_smaller_mask(t3,3);
		crypto_declassify(&t3less3,sizeof t3less3);
		if(t3less3 && ctr < len)
		{
			a[ctr++] = 1 - t3;
		}
#endif
	}
	DBENCH_STOP(*tsample);
	return ctr;
}

#define POLY_UNIFORM_ETA_NBLOCKS ((136 + STREAM256_BLOCKBYTES - 1)/STREAM256_BLOCKBYTES)
void poly_uniform_eta(poly *a, const uint8_t seed[CRHBYTES], uint16_t nonce)
{
	unsigned int ctr;
	unsigned int buflen = POLY_UNIFORM_ETA_NBLOCKS * STREAM256_BLOCKBYTES;
	uint8_t buf[POLY_UNIFORM_ETA_NBLOCKS*STREAM256_BLOCKBYTES];
	stream256_state state;

	stream256_init(&state, seed, nonce);
	stream256_squeezeblocks(buf, POLY_UNIFORM_ETA_NBLOCKS, &state);

	ctr = rej_eta(a->coeffs, N, buf, buflen);

	while(ctr < N)
	{
		stream256_squeezeblocks(buf, 1, &state);
		ctr += rej_eta(a->coeffs + ctr, N - ctr, buf, STREAM256_BLOCKBYTES);
	}
}

#define POLY_UNIFORM_GAMMA1_NBLOCKS ((POLYZ_PACKEDBYTES + STREAM256_BLOCKBYTES - 1)/STREAM256_BLOCKBYTES)

void poly_uniform_gamma1(poly *a, const uint8_t seed[CRHBYTES], uint16_t nonce)
{
	uint8_t buf[POLY_UNIFORM_GAMMA1_NBLOCKS*STREAM256_BLOCKBYTES];
	stream256_state state;

	stream256_init(&state, seed, nonce);
	stream256_squeezeblocks(buf, POLY_UNIFORM_GAMMA1_NBLOCKS, &state);
	polyz_unpack(a, buf);
}

//sample in ball
void poly_challenge(poly *c, const uint8_t seed[SEEDBYTES])
{
	unsigned int i, b, pos;
	uint32_t signs;
	uint8_t buf[SHAKE256_RATE];
	keccak_state state;

	shake256_init(&state);
	shake256_absorb(&state, seed, SEEDBYTES);
	shake256_finalize(&state);
	shake256_squeezeblocks(buf, 1, &state);

	signs = 0;
	for(i = 0 ; i < 4 ; i++)
		signs |= (uint32_t)buf[i] << 8 * i;
	pos = 4;

	for(i = 0 ; i < N ; i++)
		c->coeffs[i] = 0;
#if N == 2304
	for(i = N - TAU ; i < N ; i++)
	{
		do
		{
			if(pos >= SHAKE256_RATE)
			{
				shake256_squeezeblocks(buf, 1, &state);
				pos = 0;
			}

			b = (uint32_t)buf[pos++] << 4;
			b |= (buf[pos++] & 0xF);
			crypto_declassify(&b,sizeof b);		// patch
		} while(b > i);

		c->coeffs[i] = c->coeffs[b];
		c->coeffs[b] = 1 - 2 * (crypto_int64_bottombit_01(signs));
		signs >>= 1;
	}
#else
	for(i = N - TAU ; i < N ; i++)
	{
		do
		{
			if(pos >= SHAKE256_RATE)
			{
				shake256_squeezeblocks(buf, 1, &state);
				pos = 0;
			}

			b = (uint32_t)buf[pos++] << 3;
			b |= (buf[pos++] & 0x7);
			crypto_declassify(&b,sizeof b);		// patch
		} while(b > i);

		c->coeffs[i] = c->coeffs[b];
		c->coeffs[b] = 1 - 2 * (crypto_int64_bottombit_01(signs));
		signs >>= 1;
	}
#endif
}

